# Entrega de datos
print("-"*5, "número", "-"*5)
numeroA = input("Digita un número entero: ")
numeroB = input("Digite otro número entero: ")
numA = int (numeroA)
numB = int (numeroB)

# Suma de datos
print("-"*5, "suma", "-"*5)
sumar = numA + numB
print(f"la suma dio {sumar} exitosamente")

# Resta de datos
print("-"*5, "resta", "-"*5)
resta = numA - numB
print(f"la resta dio {resta} exitosamente")

# Mutiplicación de datos
print("-"*5, "multiplicación", "-"*5)
multiplicar = numA * numB
print(f"la Multiplicación dio {multiplicar} exitosamente")